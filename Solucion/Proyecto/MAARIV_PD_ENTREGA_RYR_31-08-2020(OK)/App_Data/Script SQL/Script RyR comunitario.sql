	JOIN [dbo].[TBL_RPERSONA] AS P ON HP.ID_PERSONA = P.ID_PERSONA 
	JOIN [dbo].[TIPO_IDENTIFICACION] AS IDE ON P.TDO_NID = IDE.ID_TIPO_IDENTIFICACION
	join [dbo].[RYR_CONFORMACION_HOGAR] as CH ON P.ID_PERSONA = CH.ID_PERSONA
